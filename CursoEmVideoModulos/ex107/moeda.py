def dobro(valor):
    resultado = 2 * valor
    return resultado


def metade(valor):
    resultado = valor / 2
    return resultado


def aumentar(valor, porcentagem):
    acrescimo = porcentagem / 100 * valor
    resultado = valor + acrescimo
    return resultado


def reduzir(valor, porcentagem):
    decrescimo = porcentagem / 100 * valor
    resultado = valor - decrescimo
    return resultado


